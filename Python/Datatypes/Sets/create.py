#create a set
set1 = set()
print(set1)

#creating a set function

set2 = set("Hello")
print(type(set2))
print(set2)

#create using list
set3 = set([1,2, 3, 4])
print(type(set3))
print(set3)

#create using dictionary
set4 = set({"Name": 3, "Menu": 4})
print(type(set4))
print(set4)

#create using tuple
tup = ("Hello", "Hello", "Man", "Man", "How")
set5 = set(tup)
print(type(set5))
print(set5)
