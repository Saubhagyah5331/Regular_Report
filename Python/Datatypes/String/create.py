# Creating a String
# Strings can be created using either single (‘) or double (“) quotes.

s = 'Saubhagya'
s2 = "Saubhagya2"

print(s)
print(s2)

# Multi-line Strings
# If we need a string to span multiple lines then we can use triple quotes (”’ or “””).

s3 = """My name is Saubhagya
and I am from Jamnagar"""

print(s3)
