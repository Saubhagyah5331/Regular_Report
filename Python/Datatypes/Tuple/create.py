# Creating a Tuple
# A tuple is created by placing all the items inside parentheses (), separated by commas. A tuple can have any number of items and they can be of different data types.

#creating a empty tuple
tup = ()
print(tup)

# Creating with strings
tup_string = ("Helllo", "Hell", "Angel")
print(tup_string)

#creating with list
li = [2, 34, 1, 3]
print(tuple(li))

#Creating using Built-in function
tup_built_in = tuple(("Geeks","Hello"))
print(tup_built_in)